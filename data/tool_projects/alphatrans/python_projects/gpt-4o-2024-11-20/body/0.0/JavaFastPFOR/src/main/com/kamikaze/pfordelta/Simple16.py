    __S16_NUM: typing.List[int] = [28, 21, 21, 21, 14, 9, 8, 7, 6, 6, 5, 5, 4, 3, 2, 1]
    __S16_BITSSIZE: int = 28
    __S16_NUMSIZE: int = 16

    @staticmethod
    def s16Decompress(
        out: typing.List[int],
        outOffset: int,
        in_: typing.List[int],
        inOffset: int,
        n: int,
    ) -> int:
        numIdx = in_[inOffset] >> Simple16.__S16_BITSSIZE
        num = min(Simple16.__S16_NUM[numIdx], n)
        bits = 0
        for j in range(num):
            out[outOffset + j] = Simple16.__readBitsForS16(
                in_, inOffset, bits, Simple16.__S16_BITS[numIdx][j]
            )
            bits += Simple16.__S16_BITS[numIdx][j]
        return num

    @staticmethod
    def s16Compress(
        out: typing.List[int],
        outOffset: int,
        in_: typing.List[int],
        inOffset: int,
        n: int,
        blockSize: int,
    ) -> int:
        numIdx = 0
        j = 0
        num = 0
        bits = 0

        try:
            for numIdx in range(Simple16.__S16_NUMSIZE):
                out[outOffset] = numIdx << Simple16.__S16_BITSSIZE
                num = min(Simple16.__S16_NUM[numIdx], n)

                for j in range(num):
                    if in_[inOffset + j] >= (1 << Simple16.__S16_BITS[numIdx][j]):
                        break
                    out[outOffset] |= in_[inOffset + j] << bits
                    bits += Simple16.__S16_BITS[numIdx][j]

                if j == num:
                    return num

        except Exception as e:
            print(f"s16Compress: numIdx: {numIdx}, j: {j}, num: {num}, bits: {bits}")
            print(e)

        return -1

    @staticmethod
    def __readBitsForS16(
        in_: typing.List[int], inIntOffset: int, inWithIntOffset: int, bits: int
    ) -> int:
        val = in_[inIntOffset] >> inWithIntOffset
        return val & (0xFFFFFFFF >> (32 - bits))
